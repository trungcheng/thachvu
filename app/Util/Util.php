    public static function skuGenerate($length, $productId) {
        $result = '';
        for ($i = 0; $i < $length; $i++) {
            $result .= mt_rand(0, 9);
        }

        return $result.$productId;
    }

    public static function buildTree(array $elements, $parentId = 0) {
        $branch = array();
        foreach ($elements as $element) {
            if ($element['parent_id'] == $parentId) {
                $children = self::buildTree($elements, $element['id']);
                if ($children) {
                    $element['childrens'] = $children;
                }
                $branch[] = $element;
            }
        }
        
        return $branch;
    }

    public static function buildArray(array $objects, array &$result=array(), $parent=0, $depth=0)
    {
        foreach ($objects as $key => $object) {
            if ($object['parent_id'] == $parent) {
                $object['depth'] = $depth;
                array_push($result, $object);
                unset($objects[$key]);
                self::buildArray($objects, $result, $object['id'], $depth + 1);
            }
        }

        return $result;
    }

